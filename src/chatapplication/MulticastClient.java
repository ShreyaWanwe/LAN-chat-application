    private void sendFileButtonActionPerformed(java.awt.event.ActionEvent evt) {
        javax.swing.JFileChooser fileChooser = new javax.swing.JFileChooser();
        int result = fileChooser.showOpenDialog(this);
        if (result == javax.swing.JFileChooser.APPROVE_OPTION) {
            java.io.File file = fileChooser.getSelectedFile();
            try {
                // Start a server socket to send the file
                java.net.ServerSocket fileServer = new java.net.ServerSocket(0); // random free port
                int port = fileServer.getLocalPort();
                String localIP = java.net.InetAddress.getLocalHost().getHostAddress();
                String fileMsg = "[FILE]" + file.getName() + "|" + file.length() + "|" + localIP + "|" + port;
                // Multicast file info
                byte[] buf = (name + ": " + fileMsg).getBytes();
                InetAddress group = InetAddress.getByName("230.0.0.1");
                DatagramPacket packet = new DatagramPacket(buf, buf.length, group, 4446);
                s.send(packet);
                chatTextArea.append("[File sharing started: " + file.getName() + "]\n");
                // Start a thread to send the file
                new Thread(() -> {
                    try (java.net.Socket client = fileServer.accept();
                         java.io.FileInputStream fis = new java.io.FileInputStream(file);
                         java.io.OutputStream os = client.getOutputStream()) {
                        byte[] buffer = new byte[4096];
                        int read;
                        while ((read = fis.read(buffer)) != -1) {
                            os.write(buffer, 0, read);
                        }
                        os.flush();
                        chatTextArea.append("[File sent: " + file.getName() + "]\n");
                    } catch (Exception ex) {
                        chatTextArea.append("[File send failed: " + ex.getMessage() + "]\n");
                    } finally {
                        try { fileServer.close(); } catch (Exception ex) {}
                    }
                }).start();
            } catch (Exception ex) {
                chatTextArea.append("[File sharing error: " + ex.getMessage() + "]\n");
            }
        }
    }
