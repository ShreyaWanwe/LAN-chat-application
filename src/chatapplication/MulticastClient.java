private void startScreenShareButtonActionPerformed(java.awt.event.ActionEvent evt) { 
    startScreenShareButton.setEnabled(false);
    stopScreenShareButton.setEnabled(true);

    try {
        String ip = InetAddress.getLocalHost().getHostAddress();   // get this machine's IP
        String screenShareMsg = "***** " + name + " started screen sharing at " + ip + ":5555 *****";

        byte[] buf = screenShareMsg.getBytes();
        InetAddress group = InetAddress.getByName("230.0.0.1");
        DatagramPacket packet = new DatagramPacket(buf, buf.length, group, 4446);
        s.send(packet);
    } catch (Exception e) {
        chatTextArea.append("[Screen share notification error: " + e.getMessage() + "]\n");
    }

    // ✅ Start Screen Share Server
    try {
        screenShareServerThread = new Thread(new ScreenShareServer(5555));
        screenShareServerThread.start();
        chatTextArea.append("[Screen sharing started on port 5555]\n");
    } catch (Exception e) {
        chatTextArea.append("[Error starting screen share: " + e.getMessage() + "]\n");
    }
}



private void stopScreenShareButtonActionPerformed(java.awt.event.ActionEvent evt) { 
    startScreenShareButton.setEnabled(true);
    stopScreenShareButton.setEnabled(false);

    String screenShareMsg = "***** " + name + " stopped screen sharing *****";
    try {
        byte[] buf = screenShareMsg.getBytes();
        InetAddress group = InetAddress.getByName("230.0.0.1");
        DatagramPacket packet = new DatagramPacket(buf, buf.length, group, 4446);
        s.send(packet);
    } catch (Exception e) {
        chatTextArea.append("[Screen share notification error: " + e.getMessage() + "]\n");
    }

    // ✅ Stop server
    try {
        if (screenShareServerThread != null && screenShareServerThread.isAlive()) {
            screenShareServerThread.interrupt();
            screenShareServerThread = null;
        }
        chatTextArea.append("[Screen sharing stopped]\n");
    } catch (Exception e) {
        chatTextArea.append("[Error stopping screen share: " + e.getMessage() + "]\n");
    }
}


